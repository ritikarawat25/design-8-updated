    <script>
        document.addEventListener("DOMContentLoaded", function () {
            const inputs = document.querySelectorAll(".phone-input");

            inputs.forEach(input => {
                var iti = window.intlTelInput(input, {
                    nationalMode: true,
                    preferredCountries: ['ae'],
                    utilsScript: "https://cdn.jsdelivr.net/npm/intl-tel-input@19.2.16/build/js/utils.js",
                });

                function updateDialCode() {
                    var selectedCountryData = iti.getSelectedCountryData();
                    $(input.parentNode).find('#country-code-apnd').remove();
                    $(input.parentNode).find('.iti__selected-flag').append("<div id='country-code-apnd'>  " + '+' + selectedCountryData.dialCode + "</div>");
                }

                input.addEventListener("countrychange", updateDialCode);
                updateDialCode();
            });
        });
    </script>
